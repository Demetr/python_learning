# int, float, complex Data types
# Creating an integer
num = 10
print(type(num))  # Output: <class 'int'>

# Creating a float
num = 10.5
print(type(num))  # Output: <class 'float'>

# Creating a complex number
num = 1 + 2j
print(type(num))  # Output: <class 'complex'>

# Performing operations
int_num = 10
float_num = 5.5
complex_num = 1 + 2j

# Addition
print(int_num + float_num)  # Output: 15.5

# Subtraction
print(float_num - int_num)  # Output: -4.5

# Multiplication
print(int_num * complex_num)  # Output: (10+20j)